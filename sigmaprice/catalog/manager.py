"""Catalog item business logic (create, update, delete, search)"""
from typing import Optional, List, Dict, Any
from sqlalchemy.orm import Session
from sigmaprice.db.models import CatalogItem as CatalogItemModel
from sigmaprice.core.types import RawItem, MatchResult
from sigmaprice.core.exceptions import ValidationError, DatabaseError
from sigmaprice.core.logger import get_logger
from sigmaprice.catalog.code_generator import generate_code
from sigmaprice.catalog.name_builder import build_name
from sigmaprice.catalog.validators import (
    validate_name,
    validate_ean,
    determine_delivery_type,
    is_excluded_delivery,
)
from sigmaprice.catalog.categories import determine_category

logger = get_logger(__name__)

FORBIDDEN_UPDATE_FIELDS = {"id", "code", "created_at", "updated_at"}


def create_item(
    item: RawItem,
    match_result: MatchResult,
    session: Optional[Session] = None,
) -> CatalogItemModel:
    """
    Create a new catalog item from RawItem and MatchResult.

    Algorithm:
    1. Check for excluded delivery types
    2. Generate unique 8-digit code
    3. Build normalized name
    4. Determine category
    5. Determine delivery type
    6. Save to DB
    7. Generate embedding (delegated to caller or triggered separately)

    Raises:
        ValidationError: if required fields are empty
        DatabaseError: if code generation fails after max retries
    """
    from sigmaprice.core.database import get_session
    if session is None:
        session = get_session()

    name = validate_name(item.name)

    if is_excluded_delivery(name):
        raise ValidationError(
            f"Item excluded due to damaged/defective packaging: {name}"
        )

    code = generate_code(session)

    category_id = determine_category(item, session)

    category_name = _get_category_name(category_id, session)
    normalized_name = build_name(item, category_name)

    delivery_type = determine_delivery_type(
        item.name, getattr(item, 'delivery_type', None)
    )

    try:
        catalog_item = CatalogItemModel(
            code=code,
            name=normalized_name,
            description=item.description,
            manufacturer=item.manufacturer,
            article=item.article,
            ean=validate_ean(item.ean),
            category_id=category_id,
            country_origin=getattr(item, 'country_origin', None),
            delivery_type=delivery_type,
        )
        session.add(catalog_item)
        session.flush()

        _save_supplier_mapping(catalog_item.id, item, session)

        session.commit()
        session.refresh(catalog_item)

        logger.info(
            f"Created catalog item: {catalog_item.code} - {catalog_item.name}"
        )
        return catalog_item

    except Exception as e:
        session.rollback()
        logger.error(f"Failed to create catalog item: {e}")
        raise DatabaseError(f"Failed to create catalog item: {e}")


def get_item(
    item_id: int,
    session: Optional[Session] = None,
) -> Optional[CatalogItemModel]:
    """Get catalog item by ID."""
    from sigmaprice.core.database import get_session
    if session is None:
        session = get_session()
    return (
        session.query(CatalogItemModel)
        .filter(CatalogItemModel.id == item_id)
        .first()
    )


